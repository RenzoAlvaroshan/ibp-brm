export type Locale = keyof typeof translations
export type TranslationKey = keyof typeof translations.en

function getLocale(): Locale {
  if (typeof window === 'undefined') return DEFAULT_LOCALE
  const stored = window.localStorage.getItem('brm-locale')
  return stored === 'id' ? 'id' : DEFAULT_LOCALE
}

export function translate(key: TranslationKey, locale: Locale = getLocale()): string {
  return translations[locale][key] ?? translations.en[key] ?? key
}

export function useI18n() {
  const locale = getLocale()
  return {
    locale,
    t: (key: TranslationKey) => translate(key, locale),
  }
}
